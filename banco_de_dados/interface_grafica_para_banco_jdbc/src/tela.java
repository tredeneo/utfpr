    private void mostrar_tabela(String query){
	intro_banco_de_dados.carrega_codigo(query);
        var objeto = new Object[20][];
        for(int i = 0; i < 20 ;i++)
        {		
		var linha = intro_banco_de_dados.pega_colunas();
		objeto[i] = linha.toArray();       
        }

        tabela.setModel(new javax.swing.table.DefaultTableModel(
                objeto,intro_banco_de_dados.nome_colunas().stream().toArray()));
    }
    
