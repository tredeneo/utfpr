    private void abrir_conexao(){

	    try {
		    usuario_entrada.setText(new String(Files.readAllBytes(Paths.get("usuario.txt"))));
		    banco_entrada.setSelectedItem(new String(Files.readAllBytes(Paths.get("banco.txt"))));
		    
		    endereco_entrada.setSelectedItem(new String(Files.readAllBytes(Paths.get("endereco.txt"))));
		    senha_entrada.setText(Files.readString(Paths.get("senha.txt")));
		    System.out.println(Files.readString(Paths.get("senha.txt")));
	    } catch (IOException ignored) {
	    }
    }
    
    private void salvar_conexao() {
	    try {
		    var str_usuario = new PrintWriter("usuario.txt", "UTF-8");
		    var str_senha = new PrintWriter("senha.txt", "UTF-8");
		    var str_endereco = new PrintWriter("endereco.txt", "UTF-8");
		    var str_banco = new PrintWriter("banco.txt", "UTF-8");
		    str_usuario.print(usuario_entrada.getText());
		    str_usuario.close();
		    str_senha.print(senha_entrada.getPassword());
		    str_senha.close();
		    str_endereco.print(endereco_entrada.getSelectedItem());
		    str_endereco.close();
		    str_banco.print(banco_entrada.getSelectedItem());
		    str_banco.close();
		    str_usuario.close();
	    } catch (FileNotFoundException | UnsupportedEncodingException ignored) {
	    }
    }
    private void mostrar_bancosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mostrar_bancosActionPerformed
        // TODO add your handling code here:
        var  str_banco = lista_bancos.getSelectedItem()+"";
	prepara_banco("show tables", str_banco);
        var campos = intro_banco_de_dados.pega_linhas();
        lista_tabelas.removeAllItems();
        campos.forEach(aux -> {
                lista_tabelas.addItem(aux);
        });
	espacio_algoritmo.setEnabled(true);
	executar.setEnabled(true);
	lista_tabelas.setEnabled(true);
	mostrar_tabela.setEnabled(true);
	mostrar_metadados.setEnabled(true);
        
    }//GEN-LAST:event_mostrar_bancosActionPerformed

    private void prepara_banco(String query,String banco_dados){
        var str_banco_tipo = banco_entrada.getSelectedItem()+"";
        var str_usuario = usuario_entrada.getText();
        var str_senha = new String(senha_entrada.getPassword());
	var str_endereco = endereco_entrada.getSelectedItem()+"";
        intro_banco_de_dados.carrega_driver(str_banco_tipo,banco_dados,str_endereco,str_usuario,str_senha);
        intro_banco_de_dados.carrega_codigo(query);
    }
        
